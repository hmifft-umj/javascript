console.log("Assignment Operators");
i = 6;
j = 3;
//perhatikan nilai i yang selalu berubah-ubah
console.log(i = j); // i = j, nilai i = 3
console.log(i += j); // i = i + j, nilai i = 6
console.log(i -= j); // i = i - j, nilai i = 3
console.log(i *= j); // i = i * j, nilai i = 9
console.log(i **= j); // i = i ** j, nilai i = 729
console.log(i /= j); // i = i / j, nilai i = 243
console.log(i %= j); // i = i % j, nilai i = 0
console.log("Nilai i sekarang adalah", i); // i = 0